<h3>Etymology</h3>
<p>The word whisky (or whiskey) is an anglicisation of the Classical Gaelic word uisce (or uisge) meaning "water" (now written as uisce in Irish Gaelic, and uisge in Scottish Gaelic). Distilled alcohol was known in Latin as aqua vitae ("water of life"). This was translated to Classical Gaelic as Irish: uisce beatha/Scottish Gaelic: uisge beatha "water of life". Early forms of the word in English included uskebeaghe (1581), usquebaugh (1610), usquebath (1621), and usquebae (1715).</p>
