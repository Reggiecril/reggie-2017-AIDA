<h3>Chemistry</h3>
<p>Whiskies and other distilled beverages, such as cognac and rum, are complex beverages that contain a vast range of flavouring compounds, of which some 200 to 300 are easily detected by chemical analysis. The flavouring chemicals include "carbonyl compounds, alcohols, carboxylic acids and their esters, nitrogen- and sulphur-containing compounds, tannins, and other polyphenolic compounds, terpenes, and oxygen-containing heterocyclic compounds" and esters of fatty acids. The nitrogen compounds include pyridines, picolines and pyrazines.The sulfur compounds include thiophenes and polysulfides which seem to contribute to whiskey s roasted character</p>
