<h3>Types</h3>
<p>Single malt whisky is whisky from a single distillery made from a mash that uses only one particular malted grain</p>
<p>Blended malt whisky is a mixture of single malt whiskies from different distilleries.</p>
<p>Blended whisky is made from a mixture of different types of whisky.</p>
<p>Cask strength (also known as barrel proof) whiskies are rare, and usually only the very best whiskies are bottled in this way.</p>
<p>Single cask (also known as single barrel) whiskies are bottled from an individual cask, and often the bottles are labelled with specific barrel and bottle numbers.</p>
